package com.example.police_app2;

public class DtaPoint {
    int xValue,yValue;

    public DtaPoint(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public DtaPoint() {
    }

    public int getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }

}
